//! The `kernel`

#![feature(asm)]
#![feature(global_asm)]
#![no_main]
#![no_std]

// Conditionally includes the selected `architecture` code, which provides the `_start()` function,
// the first function to run.
mod arch;

// Conditionally includes the selected `BSP` code.
mod bsp;

mod panic_wait;

/// Wait
#[no_mangle]
pub unsafe extern "C" fn kmain() -> ! {
    loop {
        asm!("wfe" :::: "volatile")
    }
}
